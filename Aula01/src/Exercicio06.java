/*
 Exercício 06: Estrutura de Looping - Repetição - PARA (FOR)
 */

public class Exercicio06 {
    public static void main(String[] args) {
        System.out.println("Mostrar na tela os números inteiros de 0 a 100");
        
        for(int i=0;i<=100;i++){
            System.out.println(i);
        }
    }
}
