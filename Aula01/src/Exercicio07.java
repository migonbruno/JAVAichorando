/*
 Exercício 07: Ordem inversa com o FOR
 */
public class Exercicio07 {
    public static void main(String[] args) {
        System.out.println("Mostrar na tela os números inteiros de 0 a 100 na ordem inversa");
        
        for(int i=100;i>=0;i--){
            System.out.println(i);
        }
    }
}
